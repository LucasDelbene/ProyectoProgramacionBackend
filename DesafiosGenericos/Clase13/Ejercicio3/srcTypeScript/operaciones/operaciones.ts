//CREO UN export DE UNA VARIABLE const DE UNA FUNCION, EN LA CUAL CREO OPERACIONES DE SUMA, RESTA, MULTIPLICACION y DIVISION//

export const sumar = (a:number, b:number):number => a+b;        //SUMA 
export const restar = (a:number, b:number):number => a-b;       //RESTA
export const multiplicar = (a:number, b:number):number => a*b;  //MULTIPLICACION
export const dividir = (a:number, b:number):number => a/b;      //DIVISION